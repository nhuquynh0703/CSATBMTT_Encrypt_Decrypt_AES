    // =========================================================
    // NHAN TRONG GF(2^8)
    // =========================================================
    static int gmul(int a, int b) {
        int p = 0;
        for (int i = 0; i < 8; i++) {
            if ((b & 1) != 0)
                p ^= a;
            boolean hi = (a & 0x80) != 0;
            a = (a << 1) & 0xFF;
            if (hi)
                a ^= 0x1b;
            b >>= 1;
        }
        return p & 0xFF;
    }

    // =========================================================
    // KEY EXPANSION - ho tro 128/192/256 bit
    // NK = so words trong key: 4(128), 6(192), 8(256)
    // NR = so vong: 10(128),12(192),14(256)
    // =========================================================
    public int[][] keyExpansion(byte[] key) {
        int NK = key.length / 4; // 4, 6, hoac 8
        int NR = NK + 6; // 10, 12, hoac 14
        int totalWords = 4 * (NR + 1); // 44, 52, hoac 60

        int[][] w = new int[totalWords][4];

        for (int i = 0; i < NK; i++) {
            w[i][0] = key[4 * i] & 0xFF;
            w[i][1] = key[4 * i + 1] & 0xFF;
            w[i][2] = key[4 * i + 2] & 0xFF;
            w[i][3] = key[4 * i + 3] & 0xFF;
        }

        for (int i = NK; i < totalWords; i++) {
            int[] temp = w[i - 1].clone();
            if (i % NK == 0) {
                // RotWord
                int t = temp[0];
                temp[0] = temp[1];
                temp[1] = temp[2];
                temp[2] = temp[3];
                temp[3] = t;
                // SubWord
                for (int j = 0; j < 4; j++)
                    temp[j] = SBOX[temp[j]];
                // XOR Rcon
                temp[0] ^= RCON[i / NK - 1];
            } else if (NK > 6 && i % NK == 4) {
                // SubWord chi cho AES-256
                for (int j = 0; j < 4; j++)
                    temp[j] = SBOX[temp[j]];
            }
            for (int j = 0; j < 4; j++)
                w[i][j] = w[i - NK][j] ^ temp[j];
        }
        return w;
    }

    // =========================================================
    // CAC PHEP BIEN DOI
    // =========================================================
    private int getNR(int[][] roundKeys) {
        return roundKeys.length / 4 - 1;
    }

    void addRoundKey(int[][] state, int[][] w, int round) {
        for (int col = 0; col < 4; col++)
            for (int row = 0; row < 4; row++)
                state[row][col] ^= w[round * 4 + col][row];
    }

    void subBytes(int[][] s) {
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++)
                s[r][c] = SBOX[s[r][c]];
    }

    void invSubBytes(int[][] s) {
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++)
                s[r][c] = INV_SBOX[s[r][c]];
    }

    void shiftRows(int[][] s) {
        int t;
        t = s[1][0];
        s[1][0] = s[1][1];
        s[1][1] = s[1][2];
        s[1][2] = s[1][3];
        s[1][3] = t;
        t = s[2][0];
        s[2][0] = s[2][2];
        s[2][2] = t;
        t = s[2][1];
        s[2][1] = s[2][3];
        s[2][3] = t;
        t = s[3][3];
        s[3][3] = s[3][2];
        s[3][2] = s[3][1];
        s[3][1] = s[3][0];
        s[3][0] = t;
    }

    void invShiftRows(int[][] s) {
        int t;
        t = s[1][3];
        s[1][3] = s[1][2];
        s[1][2] = s[1][1];
        s[1][1] = s[1][0];
        s[1][0] = t;
        t = s[2][0];
        s[2][0] = s[2][2];
        s[2][2] = t;
        t = s[2][1];
        s[2][1] = s[2][3];
        s[2][3] = t;
        t = s[3][0];
        s[3][0] = s[3][1];
        s[3][1] = s[3][2];
        s[3][2] = s[3][3];
        s[3][3] = t;
    }

    void mixColumns(int[][] s) {
        for (int c = 0; c < 4; c++) {
            int s0 = s[0][c], s1 = s[1][c], s2 = s[2][c], s3 = s[3][c];
            s[0][c] = gmul(2, s0) ^ gmul(3, s1) ^ s2 ^ s3;
            s[1][c] = s0 ^ gmul(2, s1) ^ gmul(3, s2) ^ s3;
            s[2][c] = s0 ^ s1 ^ gmul(2, s2) ^ gmul(3, s3);
            s[3][c] = gmul(3, s0) ^ s1 ^ s2 ^ gmul(2, s3);
        }
    }

    void invMixColumns(int[][] s) {
        for (int c = 0; c < 4; c++) {
            int s0 = s[0][c], s1 = s[1][c], s2 = s[2][c], s3 = s[3][c];
            s[0][c] = gmul(0x0e, s0) ^ gmul(0x0b, s1) ^ gmul(0x0d, s2) ^ gmul(0x09, s3);
            s[1][c] = gmul(0x09, s0) ^ gmul(0x0e, s1) ^ gmul(0x0b, s2) ^ gmul(0x0d, s3);
            s[2][c] = gmul(0x0d, s0) ^ gmul(0x09, s1) ^ gmul(0x0e, s2) ^ gmul(0x0b, s3);
            s[3][c] = gmul(0x0b, s0) ^ gmul(0x0d, s1) ^ gmul(0x09, s2) ^ gmul(0x0e, s3);
        }
    }

    // =========================================================
    // MA HOA 1 BLOCK
    // =========================================================
    public byte[] encryptBlock(byte[] input, int[][] roundKeys) {
        int NR = getNR(roundKeys);
        int[][] state = new int[4][4];
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++)
                state[r][c] = input[r + 4 * c] & 0xFF;

        addRoundKey(state, roundKeys, 0);
        for (int round = 1; round < NR; round++) {
            subBytes(state);
            shiftRows(state);
            mixColumns(state);
            addRoundKey(state, roundKeys, round);
        }
        subBytes(state);
        shiftRows(state);
        addRoundKey(state, roundKeys, NR);

        byte[] out = new byte[16];
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++)
                out[r + 4 * c] = (byte) state[r][c];
        return out;
    }

    // =========================================================
    // GIAI MA 1 BLOCK
    // =========================================================
    public byte[] decryptBlock(byte[] input, int[][] roundKeys) {
        int NR = getNR(roundKeys);
        int[][] state = new int[4][4];
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++)
                state[r][c] = input[r + 4 * c] & 0xFF;

        addRoundKey(state, roundKeys, NR);
        for (int round = NR - 1; round >= 1; round--) {
            invShiftRows(state);
            invSubBytes(state);
            addRoundKey(state, roundKeys, round);
            invMixColumns(state);
        }
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, roundKeys, 0);

        byte[] out = new byte[16];
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++)
                out[r + 4 * c] = (byte) state[r][c];
        return out;
    }
